# Arithmetic Operators
a = 10
b = 3
print("Arithmetic Operators")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)
print("a // b =", a // b)

# Comparison Operators
print("\nComparison Operators")
print("a == b :", a == b)
print("a != b :", a != b)
print("a > b  :", a > b)
print("a < b  :", a < b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)

# Assignment Operators
print("\nAssignment Operators")
c = 5
c += 2
print("c += 2 ->", c)
c -= 1
print("c -= 1 ->", c)
c *= 2
print("c *= 2 ->", c)
c /= 2
print("c /= 2 ->", c)

# Logical Operators
print("\nLogical Operators")
x = True
y = False
print("x and y :", x and y)
print("x or y  :", x or y)
print("not x   :", not x)

# Bitwise Operators
print("\nBitwise Operators")
p = 5   # 101
q = 3   # 011
print("p & q :", p & q)
print("p | q :", p | q)
print("p ^ q :", p ^ q)
print("~p    :", ~p)
print("p << 1:", p << 1)
print("p >> 1:", p >> 1)

# Membership Operators
print("\nMembership Operators")
lst = [1, 2, 3, 4]
print("2 in list     :", 2 in lst)
print("5 not in list :", 5 not in lst)

# Identity Operators
print("\nIdentity Operators")
m = 10
n = 10
print("m is n     :", m is n)
print("m is not n :", m is not n)
